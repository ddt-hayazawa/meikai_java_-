package e_02_02;

public class E_02_02 {
	// 三つのint型変数に値を代入し合計を求める
	public static void main(String[] args){
		int x; // int型の変数xを宣言
		int y; // int型の変数yを宣言
		int z; // int型の変数zを宣言

		x = 10; // int型の変数xに10を代入
		y = 20; // int型の変数yに20を代入
		z = 30; // int型の変数zに30を代入

		System.out.println("合計は" + (x + y + z) + "です。"); // 合計を表示
		System.out.println("合計は" + (x + y + z) / 2 + "です。"); // 平均を表示

	}
}
